						<p class="p-desc">La Tremenda és el drac de la secció infantil. És una estructura que simula la silueta d’un drac de dos caps. Els colors de la Tremenda provenen de la Víbria i el Drac de Manresa, mare i pare de la Tremenda. La Tremenda, durant l’any recull els xumets dels infants de la ciutat i els porta al coll durant les actuacions.<br><br>
						El Ball de la Tremenda és costum que el faci un sol ballador, tot i que en alguns moments puntuals l’han arribat a ballar dues persones.<br><br>
						Té dues pinces a la boca per on s’hi posa la pirotècnia durant el ball i els diferents correfocs que es fan durant l’any. Normalment porta 4 fuets encesos.<br><br>
